// Считаем от 1 до 10
// но рекурсивно
////////////////////////////////////////////////////////////////
#include <stdio.h>

void count(int n) {
    if (n > 10) {
        return;
    }
    printf("%d\n", n);
    count(n + 1);
}

int main() {
    count(1);
    return 0;
}
